package Estructuras_Selectivas;

public class Ciclo_Do_While {

    public static void main (String[]args){

        // Haga hasta que
        // Do while

        int Num = 1;

        do
        {
            System.out.println("Numero: " + Num);
            Num = Num + 4 ;
        }

        while (Num < 25);

    }
    
}
